def _totfact_hint(src_key):
    hints = {
        'Am-241': '3.6×10⁵  (35.9% gamma yield × 1×10⁶ Bq)',
        'Cs-137': '8.5×10⁵  (85.1% gamma yield × 1×10⁶ Bq)',
        'Co-60':  '2.0×10⁶  (~200% gamma yield × 1×10⁶ Bq)',
        'Ir-192': '8.2×10⁵  (82% gamma yield × 1×10⁶ Bq)',
        'Am-Be':  '6.5×10⁴  (65 n/s per MBq Am-241)',
        'Cf-252': '1.17×10⁸ (116600 n/s per MBq Cf-252)',
    }
    return hints.get(src_key, 'see isotope decay data')


