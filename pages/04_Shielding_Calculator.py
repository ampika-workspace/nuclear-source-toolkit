# ── Dose rate inputs ───────────────────────────────────────────────────────────
col_dr, col_target = st.columns(2, gap="large")

with col_dr:
    st.subheader("Unshielded dose rate")
    dr_mode = st.radio("Specify as", ["Calculate from activity", "Enter directly"],
                        horizontal=True)

    if dr_mode == "Calculate from activity":
        act_unit = st.selectbox("Activity unit", UNITS, key="shield_act_unit")
        act_val  = st.number_input("Activity", min_value=0.0, value=1.0,
                                    format="%.6f", key="shield_act_val")
        dist_m   = st.number_input("Distance from source (m)", min_value=0.01,
                                    value=1.0, format="%.2f", key="shield_dist")

        act_MBq = convert_to_ci(act_val, act_unit) * CI_TO_MBQ
        if source_type == "Gamma":
            dr_initial = gamma_dr_from_activity(act_MBq, GAMMA_DOSE_CONSTANT[isotope_sel], dist_m)
        else:
            nr = neutron_dr_from_activity(act_MBq, ns['neutron_yield_per_MBq'],
                                           ns['h_phi_pSv_cm2'], dist_m)
            gr = gamma_dr_from_activity(act_MBq, ns['gamma_dose_constant'], dist_m) \
                 if ns['gamma_dose_constant'] > 0 else 0.0
            dr_initial = nr + gr

        st.metric("Calculated dose rate", f"{dr_initial:.4f} μSv/h")
    else:
        dr_initial = st.number_input(
            "Dose rate without shielding (μSv/h)",
            min_value=0.001, value=100.0, format="%.3f",
            help="Measured or calculated dose rate at your reference point before shielding."
        )

with col_target:
    st.subheader("Target dose rate")
    target_mode = st.radio("Set target as", ["ICRP 103 limit", "Custom value"], horizontal=True)
    if target_mode == "ICRP 103 limit":
        limit_sel = st.selectbox("Dose limit", list(DOSE_LIMITS.keys()))
        dr_target = DOSE_LIMITS[limit_sel]['uSvh']
        st.caption(f"Target: **{dr_target} μSv/h**")
    else:
        dr_target = st.number_input("Custom target (μSv/h)",
                                     min_value=0.001, value=0.5, format="%.3f")

if dr_initial > dr_target:
    rf = dr_initial / dr_target
    st.markdown(
        f"**Required attenuation: {rf:.1f}×  |  "
        f"Reduction: {(1 - 1/rf)*100:.1f}%  |  "
        f"Equivalent to {np.log2(rf):.1f} HVLs (gamma) / {np.log(rf):.1f} relaxation lengths (neutron)**"
    )

st.divider()

# ── Results ────────────────────────────────────────────────────────────────────
if dr_initial <= dr_target:
    st.success(
        f"No shielding required — current dose rate ({dr_initial:.3f} μSv/h) "
        f"is already at or below the target ({dr_target} μSv/h)."
    )
    st.stop()

st.subheader("Required shielding thickness")

rows = []
if source_type == "Gamma":
    for mat in GAMMA_MATERIALS:
        hvl = hvl_data[mat]
        t   = gamma_thickness_cm(dr_initial, dr_target, hvl)
        rows.append({'Material': mat, 'Density (g/cm³)': GAMMA_DENSITY[mat],
                     'HVL (cm)': hvl, 'Required (cm)': round(t, 2),
                     'Required (mm)': round(t * 10, 1),
                     'Areal density (g/cm²)': round(t * GAMMA_DENSITY[mat], 1)})
else:
    for mat in NEUTRON_MATERIALS:
        lam = relax_data[mat]
        t   = neutron_thickness_cm(dr_initial, dr_target, lam)
        rows.append({'Material': mat, 'Density (g/cm³)': NEUTRON_DENSITY[mat],
                     'Relaxation length λ (cm)': lam, 'Required (cm)': round(t, 2),
                     'Required (mm)': round(t * 10, 1),
                     'Areal density (g/cm²)': round(t * NEUTRON_DENSITY[mat], 1)})

