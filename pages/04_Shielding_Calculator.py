# ── Physics ────────────────────────────────────────────────────────────────────
def gamma_thickness_cm(dr_i, dr_t, hvl):
    return hvl * np.log2(dr_i / dr_t) if dr_i > dr_t else 0.0

def neutron_thickness_cm(dr_i, dr_t, lam):
    return lam * np.log(dr_i / dr_t) if dr_i > dr_t else 0.0

def gamma_dr(dr_i, t, hvl):
    return dr_i * 0.5 ** (t / hvl)

def neutron_dr_atten(dr_i, t, lam):
    return dr_i * np.exp(-t / lam)


